#include "stator.h"

Stator::Stator()
{
    pairs = {{5,24},{14,19},{9,23},{26,13},{22,7},{20,12},{17,1},
            {21,25},{16,2},{18,3},{11,8},{10,6},{4,15},{24,5},{19,14},{23,9},{13,26},
            {7,22},{12,20},{1,17},{25,21},{2,16},{3,18},{8,11},{6,10},{15,4}};
}

int Stator::find(int letter)
{
    for (int i = 0; i < 26; i++)
    {
        if (pairs[i][0] == letter)
            return pairs[i][1];
    }
}
